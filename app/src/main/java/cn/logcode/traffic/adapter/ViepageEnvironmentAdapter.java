package cn.logcode.traffic.adapter;

import android.support.v4.app.Fragment;
import android.support.v4.app.FragmentManager;
import android.support.v4.app.FragmentPagerAdapter;

import java.util.List;

import cn.logcode.traffic.ui.fragment.DAfragments.DA1Fragment;

public class ViepageEnvironmentAdapter extends FragmentPagerAdapter {
    List<Fragment> fragmentList;
    public ViepageEnvironmentAdapter(FragmentManager fm, List<Fragment> fragmentList) {
        super(fm);
        this.fragmentList = fragmentList;
    }

    @Override
    public Fragment getItem(int position) {
        return fragmentList.get(position);
    }

    @Override
    public int getCount() {
        return fragmentList.size();
    }
}
